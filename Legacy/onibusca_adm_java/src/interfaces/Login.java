    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String strUsuario = txtUser.getText();
        String strSenha = 
                new String(txtSenha.getPassword()).trim();
        //Pega usuário burro
        if(strUsuario.equals("") || strSenha.equals("")){
            JOptionPane.showMessageDialog(
                    null, "Preencha os campos de login");
        }else{
            Conector conector = new Conector();
            Connection con = conector.getConnection();
            String sql = "Select * from admin "
                    + "where usuario=? and senha=?;";
            System.out.println(sql);
            //Prepara o sql para receber as strings
            PreparedStatement stmt = null;
            try {
                stmt = con.prepareStatement(sql);
                stmt.setString(1, strUsuario);
                stmt.setString(2, strSenha);
                System.out.println(stmt.toString());
                // armazenar o login do usuario
           
                ResultSet rs = null;
                rs = stmt.executeQuery();
                if(rs.next()){
                    this.setVisible(false);
                    Principal principal = new Principal();
                    principal.setVisible(true);
                    rs.close();
                    stmt.close();
                    con.close();
                }else{
                    //avisa o usuário q deu pau
                    JOptionPane.showMessageDialog(
                    null, "Usuário ou senha inválidos");
                }
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

