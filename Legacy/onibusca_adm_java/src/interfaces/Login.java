    //função para ajustar o tamanho das imagens no jlabel
    public void resize(JLabel lblName, String url){
    ImageIcon icon = new ImageIcon(url);
    Image img = icon.getImage();
    Image imgScale = img.getScaledInstance(lblName.getWidth(), lblName.getHeight(), Image.SCALE_SMOOTH);
    ImageIcon scaledIcon = new ImageIcon(imgScale);
    lblName.setIcon(scaledIcon);
        
    }
